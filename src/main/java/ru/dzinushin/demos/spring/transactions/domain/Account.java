package ru.dzinushin.demos.spring.transactions.domain;

public class Account {

	private int id;
	private double sum;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}	
}
